def passing(a, b):
    print(id(a), id(b), 'local')
    a = 100
    b = 200
    print(id(a), id(b), 'local after')  # 139857899015504 139857899018704 local after


c = 20
d = 50

print(id(c), id (d), 'global')  # 139741774037840 139741774038800 global  #  a == c
passing(c, d)  # 139741774037840 139741774038800 local  # b == d


