def local(a, b):
    a = 10
    b.append(80)
    return a, b
    


c = 30
d = [40, 60]

print(c, d)  # 30 [40, 60]
# local(c, d)  # 10 [40, 60, 80] якщо print() викликається у функції
print(local(c, d))  # (10, [40, 60, 80]) якщо результат повертає return


